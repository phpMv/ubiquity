class MimeType {
	
	private const DEFAULT_MIME_TYPE='text/html';
	
	public static function getMimeType(string $ext) : array {
		return self::EXT_MAP[$ext]??[self::DEFAULT_MIME_TYPE];
	}
	
	public static function getFileMimeTypes(string $file) : array {
		$pi=\pathinfo($file);
		if(isset($pi['extension'])){
			return self::EXT_MAP[$pi['extension']]??[self::DEFAULT_MIME_TYPE];
		}
		return [self::DEFAULT_MIME_TYPE];
	}
	
	public static function getFileMimeType(string $file): string{
		return \current(self::getFileMimeTypes($file));
	}
	
